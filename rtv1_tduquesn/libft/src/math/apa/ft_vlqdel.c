#include "libft_apa.h"

void		ft_vlqdel(t_vlq *a_vlq)
{
	t_u32	i;

	if (a_vlq)
	{
		if (*a_vlq)
		{
			i = 0;
			while (ft_msb((*a_vlq)[i]))
			{
				(*a_vlq)[i] = 0;
				++i;
			}
			(*a_vlq)[i] = 0;
			free(*a_vlq);
			*a_vlq = NULL;
		}
	}
}
